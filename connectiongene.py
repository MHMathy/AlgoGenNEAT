class ConnectionGene:
    def __init__(self,noeudin,noeudout,poids,actif,innovation):
        self.__noeudin = noeudin
        self.__noeudout = noeudout
        self.__poids = poids
        self.__actif = actif
        self.__innovation = innovation

    def deactive():
        __actif = False

    def get_noeudin():
        return __noeudin

    def get_noeudout():
        return __noeudout

    def get_poids():
        return __poids

    def get_actif():
        return __actif

    def get_innovation():
        return __innovation
